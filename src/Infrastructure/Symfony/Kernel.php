declare(strict_types=1);

namespace Nurschool\Notifier\Infrastructure\Symfony;

use Nurschool\Notifier\Infrastructure\Symfony\DependencyInjection\NurschoolSendgridExtension;
use Symfony\Bundle\FrameworkBundle\Kernel\MicroKernelTrait;
use Symfony\Component\DependencyInjection\ContainerBuilder;
use Symfony\Component\HttpKernel\Kernel as BaseKernel;

class Kernel extends BaseKernel
{
    use MicroKernelTrait;

    protected function build(ContainerBuilder $container)
    {
        $container->registerExtension(new NurschoolSendgridExtension());
    }
}
